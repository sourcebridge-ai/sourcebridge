// confluence_writer.go implements the Confluence storage-XHTML sink adapter
// (Workstream A1.P4).
//
// # Confluence storage format
//
// Confluence pages are stored as XHTML with two Atlassian custom namespaces:
//
//   - ac: — macros and structured content
//   - ri: — resource identifiers (page references, users, attachments)
//
// # Block ID preservation
//
// Each AST block is wrapped in a SourceBridge structured macro:
//
//	<ac:structured-macro ac:name="sourcebridge-block">
//	  <ac:parameter ac:name="id">b3f7a1...</ac:parameter>
//	  <ac:parameter ac:name="kind">paragraph</ac:parameter>
//	  <ac:parameter ac:name="owner">generated</ac:parameter>
//	  <ac:rich-text-body>
//	    <!-- rendered block content -->
//	  </ac:rich-text-body>
//	</ac:structured-macro>
//
// Confluence preserves custom macro parameters across view/edit/save cycles,
// so block IDs survive user edits to the surrounding content. The macro
// appears as a visible panel in the Confluence editor but does not clutter the
// published page — Confluence hides unknown macro bodies behind a collapsible
// "Unknown Macro" panel which still preserves the parameters.
//
// # Page reconciliation
//
// Pages are identified by the SourceBridge page ID stored as a page-level
// metadata property ("sourcebridge_page_id"). On upsert the writer:
//
//  1. Fetches the existing page (via ConfluenceClient).
//  2. Parses the block IDs and ownership from the macro parameters.
//  3. Runs block-level reconciliation ([ast.ReconcileBlocks]).
//  4. For generated blocks that changed, writes the new content.
//  5. Leaves human-edited blocks untouched.
//
// # Managed-page marker
//
// A Confluence info macro is emitted at the top of every managed page:
//
//	<ac:structured-macro ac:name="info">
//	  <ac:rich-text-body>
//	    This page is auto-generated by SourceBridge. …
//	  </ac:rich-text-body>
//	</ac:structured-macro>
//
// # Edit policy
//
// Default is local_to_sink (G.2). The caller is responsible for composing
// canonical + overlay[sink] via [ast.ComposeForSink] before passing the page
// to [ConfluenceWriter.WritePage].
//
// # Real HTTP client
//
// The [ConfluenceClient] interface is the port. Real HTTP implementations are
// out of scope for A1.P4. [MemoryConfluenceClient] is the in-memory fake used
// in tests and development.
