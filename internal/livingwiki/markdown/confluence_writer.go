// confluenceParsedBlock is the result of parsing one sourcebridge-block macro
// from stored Confluence XHTML.
type confluenceParsedBlock struct {
	id      ast.BlockID
	kind    ast.BlockKind
	owner   ast.Owner
	rawXHTML string
}

// parseConfluenceBlocks extracts all sourcebridge-block macros from XHTML,
// returning their id/kind/owner parameters and raw body content.
func parseConfluenceBlocks(xhtml []byte) []confluenceParsedBlock {
	s := string(xhtml)
	const macroOpen = `<ac:structured-macro ac:name="sourcebridge-block">`
	const macroClose = `</ac:structured-macro>`

	var results []confluenceParsedBlock
	offset := 0
	for {
		idx := strings.Index(s[offset:], macroOpen)
		if idx < 0 {
			break
		}
		start := offset + idx
		end := strings.Index(s[start:], macroClose)
		if end < 0 {
			break
		}
		end = start + end + len(macroClose)
		macroXHTML := s[start:end]

		id := extractAcParameter(macroXHTML, "id")
		kind := extractAcParameter(macroXHTML, "kind")
		owner := extractAcParameter(macroXHTML, "owner")
		body := extractRichTextBody(macroXHTML)

		if id != "" {
			results = append(results, confluenceParsedBlock{
				id:      ast.BlockID(id),
				kind:    ast.BlockKind(kind),
				owner:   ast.Owner(owner),
				rawXHTML: body,
			})
		}
		offset = end
	}
	return results
}

// extractRichTextBody returns the content between <ac:rich-text-body> tags.
func extractRichTextBody(xhtml string) string {
	const open = "<ac:rich-text-body>"
	const close = "</ac:rich-text-body>"
	start := strings.Index(xhtml, open)
	if start < 0 {
		return ""
	}
	start += len(open)
	end := strings.Index(xhtml[start:], close)
	if end < 0 {
		return xhtml[start:]
	}
	return strings.TrimSpace(xhtml[start : start+end])
}

// confluenceXHTMLToBlocks converts parsed Confluence block data to [ast.Block]
// values. Content fields are partially reconstructed from XHTML; for
// reconciliation purposes, we need at minimum the ID, Kind, and Owner so that
// human-edited blocks are recognised and preserved.
func confluenceXHTMLToBlocks(xhtml []byte) []ast.Block {
	parsed := parseConfluenceBlocks(xhtml)
	blocks := make([]ast.Block, 0, len(parsed))
	for _, p := range parsed {
		blk := ast.Block{
			ID:    p.id,
			Kind:  p.kind,
			Owner: p.owner,
		}
		// Reconstruct content from XHTML for fingerprinting.
		blk.Content = confluenceXHTMLToBlockContent(p.kind, p.rawXHTML)
		blocks = append(blocks, blk)
	}
	return blocks
}

// confluenceXHTMLToBlockContent does a best-effort reconstruction of block
// content from Confluence XHTML. Heading, paragraph, and code are recovered
// with reasonable fidelity; table and callout are partial. The primary use
// case is content-fingerprint matching during reconciliation, not perfect
// round-trip.
func confluenceXHTMLToBlockContent(kind ast.BlockKind, rawXHTML string) ast.BlockContent {
	switch kind {
	case ast.BlockKindHeading:
		return confluenceParseHeading(rawXHTML)
	case ast.BlockKindParagraph:
		return confluenceParseParagraph(rawXHTML)
	case ast.BlockKindCode:
		return confluenceParseCode(rawXHTML)
	case ast.BlockKindTable:
		return confluenceParseTable(rawXHTML)
	case ast.BlockKindCallout:
		return confluenceParseCallout(rawXHTML)
	case ast.BlockKindStaleBanner:
		return confluenceParseStaleBanner(rawXHTML)
	case ast.BlockKindFreeform:
		return ast.BlockContent{
			Freeform: &ast.FreeformContent{Raw: strings.TrimSpace(rawXHTML)},
		}
	default:
		return ast.BlockContent{
			Freeform: &ast.FreeformContent{Raw: strings.TrimSpace(rawXHTML)},
		}
	}
}

// reHeadingTag matches an opening heading tag and captures the level digit and content.
// Go's regexp does not support backreferences, so we match the open/close tags separately.
var reHeadingTag = regexp.MustCompile(`(?i)<(h[1-6])[^>]*>`)

func confluenceParseHeading(rawXHTML string) ast.BlockContent {
	m := reHeadingTag.FindStringSubmatch(rawXHTML)
	if m == nil {
		return ast.BlockContent{
			Heading: &ast.HeadingContent{Level: 1, Text: strings.TrimSpace(stripTags(rawXHTML))},
		}
	}
	tag := strings.ToLower(m[1])
	level := 1
	if len(tag) == 2 && tag[0] == 'h' {
		level = int(tag[1] - '0')
	}
	// Strip the opening tag, content between tags, closing tag.
	closeTag := "</" + tag + ">"
	text := rawXHTML
	if start := strings.Index(text, m[0]); start >= 0 {
		text = text[start+len(m[0]):]
	}
	if end := strings.Index(strings.ToLower(text), closeTag); end >= 0 {
		text = text[:end]
	}
	return ast.BlockContent{
		Heading: &ast.HeadingContent{Level: level, Text: strings.TrimSpace(stripTags(text))},
	}
}

func confluenceParseParagraph(rawXHTML string) ast.BlockContent {
	// Strip the outer <p>…</p> wrapper if present.
	inner := rawXHTML
	if i := strings.Index(inner, "<p>"); i >= 0 {
		inner = inner[i+3:]
		if j := strings.LastIndex(inner, "</p>"); j >= 0 {
			inner = inner[:j]
		}
	}
	// Reverse the inline XHTML tags back to markdown syntax.
	text := xhtmlInlineToMarkdown(strings.TrimSpace(inner))
	return ast.BlockContent{
		Paragraph: &ast.ParagraphContent{Markdown: text},
	}
}

func confluenceParseCode(rawXHTML string) ast.BlockContent {
	// Extract language from ac:parameter and body from CDATA.
	lang := extractAcParameter(rawXHTML, "language")
	body := extractCDATA(rawXHTML)
	return ast.BlockContent{
		Code: &ast.CodeContent{Language: lang, Body: body},
	}
}

// reConfluenceTable parses table cells out of simple Confluence XHTML tables.
var reTH = regexp.MustCompile(`(?i)<th[^>]*>([\s\S]*?)</th>`)
var reTD = regexp.MustCompile(`(?i)<td[^>]*>([\s\S]*?)</td>`)
var reTR = regexp.MustCompile(`(?i)<tr[^>]*>([\s\S]*?)</tr>`)

func confluenceParseTable(rawXHTML string) ast.BlockContent {
	var headers []string
	var rows [][]string

	trMatches := reTR.FindAllStringSubmatch(rawXHTML, -1)
	for i, trm := range trMatches {
		rowXHTML := trm[1]
		if i == 0 {
			// Header row — th elements.
			for _, thm := range reTH.FindAllStringSubmatch(rowXHTML, -1) {
				headers = append(headers, strings.TrimSpace(stripTags(thm[1])))
			}
			if len(headers) == 0 {
				// Headerless table — treat first tr as data.
				for _, tdm := range reTD.FindAllStringSubmatch(rowXHTML, -1) {
					headers = append(headers, strings.TrimSpace(stripTags(tdm[1])))
				}
			}
		} else {
			var row []string
			for _, tdm := range reTD.FindAllStringSubmatch(rowXHTML, -1) {
				row = append(row, strings.TrimSpace(stripTags(tdm[1])))
			}
			if len(row) > 0 {
				rows = append(rows, row)
			}
		}
	}

	return ast.BlockContent{
		Table: &ast.TableContent{Headers: headers, Rows: rows},
	}
}

func confluenceParseCallout(rawXHTML string) ast.BlockContent {
	text := strings.TrimSpace(stripTags(rawXHTML))
	return ast.BlockContent{
		Callout: &ast.CalloutContent{Kind: "note", Body: text},
	}
}

func confluenceParseStaleBanner(rawXHTML string) ast.BlockContent {
	// Best-effort: extract any text content.
	return ast.BlockContent{
		StaleBanner: &ast.StaleBannerContent{},
	}
}

// stripTags removes all XML/HTML tags from a string.
var reAnyTag = regexp.MustCompile(`<[^>]+>`)

func stripTags(s string) string {
	return reAnyTag.ReplaceAllString(s, "")
}

// extractCDATA extracts the content of the first CDATA section in s.
func extractCDATA(s string) string {
	const open = "<![CDATA["
	const close = "]]>"
	start := strings.Index(s, open)
	if start < 0 {
		return stripTags(s)
	}
	start += len(open)
	end := strings.Index(s[start:], close)
	if end < 0 {
		return s[start:]
	}
	return s[start : start+end]
}

