// notion_writer.go implements the Notion block-based sink adapter
// (Workstream A1.P4).
//
// # Notion block format
//
// Notion pages are composed of a flat list of typed block objects.
// Each block is a JSON object with the schema:
//
//	{
//	  "object": "block",
//	  "type": "<block_type>",
//	  "<block_type>": { /* type-specific payload */ },
//	  "external_id": "<sourcebridge-block-id>"
//	}
//
// The "external_id" field is used by SourceBridge to recover block IDs after
// they are stored in Notion. Notion preserves arbitrary metadata in the
// external_id field across view/edit/save cycles for API-created blocks.
//
// # AST → Notion block type mapping
//
//	AST kind       → Notion type
//	---------        -----------
//	heading        → heading_1 / heading_2 / heading_3 (by level; level 4+ → heading_3)
//	paragraph      → paragraph
//	code           → code (with language)
//	table          → table (parent) + table_row (children) — see note below
//	callout        → callout
//	embed          → embed (URL from page/block reference)
//	freeform       → paragraph (with raw content as text)
//	stale_banner   → callout (warning emoji)
//
// # Table limitations
//
// Notion's table block model requires a nested structure:
// - A "table" block containing a "children" array of "table_row" blocks.
// - The first row is the header row (table.has_column_header = true).
//
// This adapter emits the nested structure. Round-trip fidelity for tables is
// limited: reading back a table from Notion via [NotionReader] may not
// recover all content since Notion's API returns children separately from
// the parent table block.
//
// # Page reconciliation
//
// Pages are identified by the SourceBridge page ID stored as a Notion page
// property ("sourcebridge_page_id"). [NotionWriter.WritePage] performs:
//
//  1. Fetch the existing Notion page blocks via [NotionClient].
//  2. Recover block IDs from the external_id fields.
//  3. Run block-level reconciliation ([ast.ReconcileBlocks]).
//  4. Append new blocks; update changed generated blocks; leave human-edited blocks.
//
// # Managed-page marker
//
// A callout block is emitted at the top of every managed Notion page:
//
//	{
//	  "object": "block",
//	  "type": "callout",
//	  "callout": {
//	    "icon": {"type":"emoji","emoji":"ℹ️"},
//	    "rich_text": [{"type":"text","text":{"content":"This page is auto-generated by SourceBridge. …"}}]
//	  }
//	}
//
// # Edit policy
//
// Default is local_to_sink (G.2). The caller must compose canonical + overlay
// via [ast.ComposeForSink] before passing the page to [NotionWriter.WritePage].
//
// # Real HTTP client
//
// [NotionClient] is the port. Real HTTP implementations call the Notion API;
// [MemoryNotionClient] is the in-memory fake for tests.
