// MarshalNotionPage renders a page to a JSON-encoded bytes.Buffer.
// The top-level JSON is an array of block objects.
func MarshalNotionPage(page ast.Page) ([]byte, error) {
	var buf bytes.Buffer
	if err := WriteNotionBlocks(&buf, page); err != nil {
		return nil, err
	}
	return buf.Bytes(), nil
}

