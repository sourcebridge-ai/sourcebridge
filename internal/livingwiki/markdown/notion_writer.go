// notionBlocksToAST converts a slice of Notion blocks back to [ast.Block] values
// for reconciliation. The conversion is partial — it recovers ID, Kind, Owner,
// and enough content for fingerprinting.
func notionBlocksToAST(blocks []NotionBlock) []ast.Block {
	var result []ast.Block
	for _, nb := range blocks {
		blk, ok := notionBlockToAST(nb)
		if !ok {
			continue
		}
		result = append(result, blk)
	}
	return result
}

// notionBlockToAST converts one NotionBlock to an ast.Block.
// Returns (block, true) when the block has a SourceBridge external_id and
// therefore participates in reconciliation. Returns (zero, false) for the
// managed-page callout (which has no external_id) and for table_row children.
func notionBlockToAST(nb NotionBlock) (ast.Block, bool) {
	if nb.ExternalID == "" {
		return ast.Block{}, false
	}

	blk := ast.Block{
		ID:    ast.BlockID(nb.ExternalID),
		Owner: ast.OwnerGenerated, // default; owner is not stored in Notion blocks
	}

	switch nb.Type {
	case "heading_1":
		blk.Kind = ast.BlockKindHeading
		text := notionHeadingText(nb, 1)
		blk.Content = ast.BlockContent{
			Heading: &ast.HeadingContent{Level: 1, Text: text},
		}
	case "heading_2":
		blk.Kind = ast.BlockKindHeading
		text := notionHeadingText(nb, 2)
		blk.Content = ast.BlockContent{
			Heading: &ast.HeadingContent{Level: 2, Text: text},
		}
	case "heading_3":
		blk.Kind = ast.BlockKindHeading
		text := notionHeadingText(nb, 3)
		blk.Content = ast.BlockContent{
			Heading: &ast.HeadingContent{Level: 3, Text: text},
		}
	case "paragraph":
		blk.Kind = ast.BlockKindParagraph
		text := notionRichTextToString(notionParagraphRichText(nb))
		blk.Content = ast.BlockContent{
			Paragraph: &ast.ParagraphContent{Markdown: text},
		}
	case "code":
		blk.Kind = ast.BlockKindCode
		body := ""
		lang := ""
		if nb.code != nil {
			body = notionRichTextToString(nb.code.RichText)
			lang = nb.code.Language
		}
		blk.Content = ast.BlockContent{
			Code: &ast.CodeContent{Language: lang, Body: body},
		}
	case "callout":
		blk.Kind = ast.BlockKindCallout
		text := ""
		if nb.callout != nil {
			text = notionRichTextToString(nb.callout.RichText)
		}
		blk.Content = ast.BlockContent{
			Callout: &ast.CalloutContent{Kind: "note", Body: text},
		}
	case "table":
		blk.Kind = ast.BlockKindTable
		// Table content is partially recovered from the table block's children.
		blk.Content = notionTableToAST(nb)
	case "embed":
		blk.Kind = ast.BlockKindEmbed
		url := ""
		if nb.embed != nil {
			url = nb.embed.URL
		}
		blk.Content = notionEmbedURLToAST(url)
	default:
		blk.Kind = ast.BlockKindFreeform
		blk.Content = ast.BlockContent{
			Freeform: &ast.FreeformContent{Raw: nb.Type},
		}
	}

	return blk, true
}

func notionHeadingText(nb NotionBlock, level int) string {
	var rt []notionRichTextItem
	switch level {
	case 1:
		if nb.heading1 != nil {
			rt = nb.heading1.RichText
		}
	case 2:
		if nb.heading2 != nil {
			rt = nb.heading2.RichText
		}
	case 3:
		if nb.heading3 != nil {
			rt = nb.heading3.RichText
		}
	}
	return notionRichTextToString(rt)
}

func notionParagraphRichText(nb NotionBlock) []notionRichTextItem {
	if nb.paragraph != nil {
		return nb.paragraph.RichText
	}
	return nil
}

func notionRichTextToString(rt []notionRichTextItem) string {
	var sb strings.Builder
	for _, item := range rt {
		sb.WriteString(item.Text.Content)
	}
	return sb.String()
}

func notionTableToAST(nb NotionBlock) ast.BlockContent {
	var headers []string
	var rows [][]string

	for i, child := range nb.children {
		if child.Type != "table_row" || child.tableRow == nil {
			continue
		}
		cells := make([]string, len(child.tableRow.Cells))
		for j, cell := range child.tableRow.Cells {
			cells[j] = notionRichTextToString(cell)
		}
		if i == 0 {
			headers = cells
		} else {
			rows = append(rows, cells)
		}
	}
	return ast.BlockContent{
		Table: &ast.TableContent{Headers: headers, Rows: rows},
	}
}

// notionEmbedURLToAST parses a sourcebridge:// embed URL back to EmbedContent.
func notionEmbedURLToAST(url string) ast.BlockContent {
	// sourcebridge://page/<pageID>/block/<blockID>
	if strings.HasPrefix(url, "sourcebridge://page/") {
		rest := strings.TrimPrefix(url, "sourcebridge://page/")
		parts := strings.SplitN(rest, "/block/", 2)
		if len(parts) == 2 {
			return ast.BlockContent{
				Embed: &ast.EmbedContent{
					TargetPageID:  parts[0],
					TargetBlockID: ast.BlockID(parts[1]),
				},
			}
		}
		return ast.BlockContent{
			Embed: &ast.EmbedContent{TargetPageID: rest},
		}
	}
	return ast.BlockContent{
		Embed: &ast.EmbedContent{TargetPageID: url},
	}
}

