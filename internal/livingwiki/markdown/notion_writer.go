// ─────────────────────────────────────────────────────────────────────────────
// Notion block types
// ─────────────────────────────────────────────────────────────────────────────

// NotionBlock is one Notion block object. The Type field discriminates the
// payload; the payload field with the same name as Type is populated.
//
// SourceBridge stores block IDs in the ExternalID field so they survive
// Notion's own ID churn and can be parsed back during reconciliation.
type NotionBlock struct {
	Object     string          `json:"object"`
	Type       string          `json:"type"`
	ExternalID string          `json:"external_id,omitempty"`
	Payload    json.RawMessage `json:"-"` // typed payload; serialized via MarshalJSON

	// Parsed payload fields — populated by UnmarshalJSON, consumed by MarshalJSON.
	paragraph  *notionParagraph
	heading1   *notionHeading
	heading2   *notionHeading
	heading3   *notionHeading
	code       *notionCode
	callout    *notionCallout
	embed      *notionEmbed
	table      *notionTable
	tableRow   *notionTableRow
	children   []NotionBlock
}

// MarshalJSON serialises the block to JSON, placing the typed payload under
// the block's type key. Children are inlined within the payload for table rows.
func (b NotionBlock) MarshalJSON() ([]byte, error) {
	// Build a raw map so we can set the dynamic type field.
	m := map[string]interface{}{
		"object": b.Object,
		"type":   b.Type,
	}
	if b.ExternalID != "" {
		m["external_id"] = b.ExternalID
	}

	switch b.Type {
	case "paragraph":
		m["paragraph"] = b.paragraph
	case "heading_1":
		m["heading_1"] = b.heading1
	case "heading_2":
		m["heading_2"] = b.heading2
	case "heading_3":
		m["heading_3"] = b.heading3
	case "code":
		m["code"] = b.code
	case "callout":
		m["callout"] = b.callout
	case "embed":
		m["embed"] = b.embed
	case "table":
		m["table"] = b.table
		if len(b.children) > 0 {
			m["children"] = b.children
		}
	case "table_row":
		m["table_row"] = b.tableRow
	}
	return json.Marshal(m)
}

// UnmarshalJSON parses a Notion block JSON object into the typed struct.
func (b *NotionBlock) UnmarshalJSON(data []byte) error {
	// Decode into a raw map first.
	var raw map[string]json.RawMessage
	if err := json.Unmarshal(data, &raw); err != nil {
		return err
	}

	if v, ok := raw["object"]; ok {
		_ = json.Unmarshal(v, &b.Object)
	}
	if v, ok := raw["type"]; ok {
		_ = json.Unmarshal(v, &b.Type)
	}
	if v, ok := raw["external_id"]; ok {
		_ = json.Unmarshal(v, &b.ExternalID)
	}
	if v, ok := raw["children"]; ok {
		_ = json.Unmarshal(v, &b.children)
	}

	switch b.Type {
	case "paragraph":
		b.paragraph = &notionParagraph{}
		if v, ok := raw["paragraph"]; ok {
			_ = json.Unmarshal(v, b.paragraph)
		}
	case "heading_1":
		b.heading1 = &notionHeading{}
		if v, ok := raw["heading_1"]; ok {
			_ = json.Unmarshal(v, b.heading1)
		}
	case "heading_2":
		b.heading2 = &notionHeading{}
		if v, ok := raw["heading_2"]; ok {
			_ = json.Unmarshal(v, b.heading2)
		}
	case "heading_3":
		b.heading3 = &notionHeading{}
		if v, ok := raw["heading_3"]; ok {
			_ = json.Unmarshal(v, b.heading3)
		}
	case "code":
		b.code = &notionCode{}
		if v, ok := raw["code"]; ok {
			_ = json.Unmarshal(v, b.code)
		}
	case "callout":
		b.callout = &notionCallout{}
		if v, ok := raw["callout"]; ok {
			_ = json.Unmarshal(v, b.callout)
		}
	case "embed":
		b.embed = &notionEmbed{}
		if v, ok := raw["embed"]; ok {
			_ = json.Unmarshal(v, b.embed)
		}
	case "table":
		b.table = &notionTable{}
		if v, ok := raw["table"]; ok {
			_ = json.Unmarshal(v, b.table)
		}
	case "table_row":
		b.tableRow = &notionTableRow{}
		if v, ok := raw["table_row"]; ok {
			_ = json.Unmarshal(v, b.tableRow)
		}
	}
	return nil
}

// notionRichTextItem is one element of a rich_text array.
type notionRichTextItem struct {
	Type string          `json:"type"`
	Text notionTextValue `json:"text"`
}

// notionTextValue is the text content within a rich_text item.
type notionTextValue struct {
	Content string `json:"content"`
}

// notionRichText builds a single-item rich_text array with plain text.
func notionRichText(text string) []notionRichTextItem {
	return []notionRichTextItem{
		{Type: "text", Text: notionTextValue{Content: text}},
	}
}

// notionParagraph is the payload for a "paragraph" block.
type notionParagraph struct {
	RichText []notionRichTextItem `json:"rich_text"`
}

// notionHeading is the shared payload for heading_1 / heading_2 / heading_3.
type notionHeading struct {
	RichText []notionRichTextItem `json:"rich_text"`
}

// notionCode is the payload for a "code" block.
type notionCode struct {
	RichText []notionRichTextItem `json:"rich_text"`
	Language string               `json:"language"`
}

// notionCalloutIcon is the icon sub-object of a callout block.
type notionCalloutIcon struct {
	Type  string `json:"type"`
	Emoji string `json:"emoji,omitempty"`
}

// notionCallout is the payload for a "callout" block.
type notionCallout struct {
	Icon     notionCalloutIcon    `json:"icon"`
	RichText []notionRichTextItem `json:"rich_text"`
}

// notionEmbed is the payload for an "embed" block.
type notionEmbed struct {
	URL string `json:"url"`
}

// notionTable is the payload for a "table" block.
type notionTable struct {
	TableWidth      int  `json:"table_width"`
	HasColumnHeader bool `json:"has_column_header"`
	HasRowHeader    bool `json:"has_row_header"`
}

// notionTableRow is the payload for a "table_row" block.
type notionTableRow struct {
	Cells [][]notionRichTextItem `json:"cells"`
}

// NotionProperties is a typed property bag for a Notion page.
type NotionProperties map[string]string

// ─────────────────────────────────────────────────────────────────────────────
// Port interface
// ─────────────────────────────────────────────────────────────────────────────

// NotionClient is the port interface for interacting with the Notion API.
// Real implementations call the Notion REST API; [MemoryNotionClient] is the
// in-memory fake for tests.
type NotionClient interface {
	// GetPage fetches all blocks for the page identified by externalID.
	// Returns (nil, nil, nil) when no page with externalID exists yet.
	GetPage(ctx context.Context, externalID string) (blocks []NotionBlock, properties NotionProperties, err error)

	// UpsertPage creates or replaces the page identified by externalID with
	// the given blocks and properties. On update the implementation is
	// responsible for translating externalID to a Notion page ID.
	UpsertPage(ctx context.Context, externalID string, blocks []NotionBlock, properties NotionProperties) error

	// AppendBlocks adds new blocks to an existing page.
	AppendBlocks(ctx context.Context, pageExternalID string, blocks []NotionBlock) error

	// UpdateBlock replaces the content of an existing block identified by its
	// Notion block ID (not the SourceBridge external_id). In the in-memory fake
	// we identify blocks by external_id for simplicity.
	UpdateBlock(ctx context.Context, blockExternalID string, block NotionBlock) error

	// DeleteBlock removes a block from its parent page.
	DeleteBlock(ctx context.Context, blockExternalID string) error
}

// ─────────────────────────────────────────────────────────────────────────────
// In-memory fake
// ─────────────────────────────────────────────────────────────────────────────

// notionPageRecord is the in-memory storage for one Notion page.
type notionPageRecord struct {
	blocks     []NotionBlock
	properties NotionProperties
}

// MemoryNotionClient is a thread-safe in-memory implementation of [NotionClient].
type MemoryNotionClient struct {
	pages map[string]*notionPageRecord // externalID → record
}

// NewMemoryNotionClient creates an empty [MemoryNotionClient].
func NewMemoryNotionClient() *MemoryNotionClient {
	return &MemoryNotionClient{
		pages: make(map[string]*notionPageRecord),
	}
}

// GetPage implements [NotionClient].
func (m *MemoryNotionClient) GetPage(_ context.Context, externalID string) ([]NotionBlock, NotionProperties, error) {
	rec, ok := m.pages[externalID]
	if !ok {
		return nil, nil, nil
	}
	blocks := make([]NotionBlock, len(rec.blocks))
	copy(blocks, rec.blocks)
	props := make(NotionProperties, len(rec.properties))
	for k, v := range rec.properties {
		props[k] = v
	}
	return blocks, props, nil
}

// UpsertPage implements [NotionClient].
func (m *MemoryNotionClient) UpsertPage(_ context.Context, externalID string, blocks []NotionBlock, properties NotionProperties) error {
	bl := make([]NotionBlock, len(blocks))
	copy(bl, blocks)
	props := make(NotionProperties, len(properties))
	for k, v := range properties {
		props[k] = v
	}
	m.pages[externalID] = &notionPageRecord{blocks: bl, properties: props}
	return nil
}

// AppendBlocks implements [NotionClient].
func (m *MemoryNotionClient) AppendBlocks(_ context.Context, pageExternalID string, blocks []NotionBlock) error {
	rec, ok := m.pages[pageExternalID]
	if !ok {
		return fmt.Errorf("memory notion: page %q not found", pageExternalID)
	}
	rec.blocks = append(rec.blocks, blocks...)
	return nil
}

// UpdateBlock implements [NotionClient] by external_id.
func (m *MemoryNotionClient) UpdateBlock(_ context.Context, blockExternalID string, block NotionBlock) error {
	for _, rec := range m.pages {
		for i, b := range rec.blocks {
			if b.ExternalID == blockExternalID {
				rec.blocks[i] = block
				return nil
			}
		}
	}
	return fmt.Errorf("memory notion: block %q not found", blockExternalID)
}

// DeleteBlock implements [NotionClient] by external_id.
func (m *MemoryNotionClient) DeleteBlock(_ context.Context, blockExternalID string) error {
	for _, rec := range m.pages {
		for i, b := range rec.blocks {
			if b.ExternalID == blockExternalID {
				rec.blocks = append(rec.blocks[:i], rec.blocks[i+1:]...)
				return nil
			}
		}
	}
	return fmt.Errorf("memory notion: block %q not found", blockExternalID)
}

// MutateBlock directly modifies a block's rich_text content, simulating a
// Notion editor changing content. Used in integration tests to inject "human
// edits" without going through WritePage.
func (m *MemoryNotionClient) MutateBlock(pageExternalID string, blockExternalID string, newText string) error {
	rec, ok := m.pages[pageExternalID]
	if !ok {
		return fmt.Errorf("memory notion: page %q not found", pageExternalID)
	}
	for i, b := range rec.blocks {
		if b.ExternalID == blockExternalID {
			mutated := b
			switch b.Type {
			case "paragraph":
				mutated.paragraph = &notionParagraph{RichText: notionRichText(newText)}
			case "heading_1", "heading_2", "heading_3":
				h := &notionHeading{RichText: notionRichText(newText)}
				switch b.Type {
				case "heading_1":
					mutated.heading1 = h
				case "heading_2":
					mutated.heading2 = h
				case "heading_3":
					mutated.heading3 = h
				}
			case "callout":
				if mutated.callout == nil {
					mutated.callout = &notionCallout{}
				}
				mutated.callout.RichText = notionRichText(newText)
			default:
				mutated.paragraph = &notionParagraph{RichText: notionRichText(newText)}
				mutated.Type = "paragraph"
			}
			rec.blocks[i] = mutated
			return nil
		}
	}
	return fmt.Errorf("memory notion: block %q not found in page %q", blockExternalID, pageExternalID)
}

