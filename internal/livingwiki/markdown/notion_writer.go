// ─────────────────────────────────────────────────────────────────────────────
// NotionWriter
// ─────────────────────────────────────────────────────────────────────────────

// NotionWriter is the Notion sink adapter.
//
// Call [WritePage] to do the full read-diff-write cycle:
//  1. Fetch the existing Notion page blocks via [NotionClient].
//  2. Recover block IDs from external_id fields.
//  3. Run block-level reconciliation.
//  4. Write only changed generated blocks; preserve human-edited blocks.
//  5. Upsert the composed block list via [NotionClient].
type NotionWriter struct {
	client NotionClient
	cfg    NotionWriterConfig
}

// NewNotionWriter creates a [NotionWriter] using the given client.
func NewNotionWriter(client NotionClient, cfg NotionWriterConfig) *NotionWriter {
	return &NotionWriter{client: client, cfg: cfg}
}

// WritePage performs the read-diff-write cycle for one page.
//
// The incoming page's blocks carry stable IDs from the canonical AST.
//
// Algorithm:
//  1. Fetch existing Notion blocks.
//  2. Build a map of blockID → existing AST block (recovering ownership from
//     the Notion external_id and a stored owner hint).
//  3. For each incoming block: if an existing block with the same ID is
//     human-edited or human-only, preserve its content. Otherwise use incoming.
//  4. Render and upsert.
func (nw *NotionWriter) WritePage(ctx context.Context, page ast.Page) error {
	// Step 1: Fetch existing blocks.
	existingBlocks, _, err := nw.client.GetPage(ctx, page.ID)
	if err != nil {
		return fmt.Errorf("notion_writer: GetPage %q: %w", page.ID, err)
	}

	// Step 2: Build existing block map by ID.
	existingAST := notionBlocksToAST(existingBlocks)
	existingByID := make(map[ast.BlockID]ast.Block, len(existingAST))
	for _, b := range existingAST {
		existingByID[b.ID] = b
	}

	// Step 3: Compose output, preserving human-edited blocks.
	output := make([]ast.Block, len(page.Blocks))
	for i, blk := range page.Blocks {
		output[i] = blk
		if existing, ok := existingByID[blk.ID]; ok {
			if existing.Owner == ast.OwnerHumanEdited || existing.Owner == ast.OwnerHumanOnly {
				output[i].Content = existing.Content
				output[i].Owner = existing.Owner
			}
		}
	}

	// Step 4: Render to Notion blocks and upsert.
	notionBlocks := astPageToNotionBlocks(ast.Page{
		ID:         page.ID,
		Manifest:   page.Manifest,
		Blocks:     output,
		Provenance: page.Provenance,
	})

	props := NotionProperties{"sourcebridge_page_id": page.ID}
	return nw.client.UpsertPage(ctx, page.ID, notionBlocks, props)
}

// WriteNotionBlocks renders a page to a JSON array of Notion block objects and
// writes the result to w. Useful for testing the render output independently.
func WriteNotionBlocks(w io.Writer, page ast.Page) error {
	blocks := astPageToNotionBlocks(page)
	enc := json.NewEncoder(w)
	enc.SetIndent("", "  ")
	return enc.Encode(blocks)
}

// MarshalNotionBlocks renders a page to a slice of NotionBlock values.
// Exported for use by callers that need the raw block slice without encoding.
func MarshalNotionBlocks(page ast.Page) []NotionBlock {
	return astPageToNotionBlocks(page)
}

// ─────────────────────────────────────────────────────────────────────────────
// AST → Notion block rendering
// ─────────────────────────────────────────────────────────────────────────────

// managedPageCallout is the callout block emitted at the top of every managed page.
func managedPageCallout() NotionBlock {
	return NotionBlock{
		Object: "block",
		Type:   "callout",
		callout: &notionCallout{
			Icon:     notionCalloutIcon{Type: "emoji", Emoji: "ℹ️"},
			RichText: notionRichText("This page is auto-generated by SourceBridge. Edits made directly may be overwritten unless this block is human-edited."),
		},
	}
}

// astPageToNotionBlocks converts an [ast.Page] to a slice of [NotionBlock]s,
// prepending the managed-page callout.
func astPageToNotionBlocks(page ast.Page) []NotionBlock {
	out := []NotionBlock{managedPageCallout()}
	for _, blk := range page.Blocks {
		nb := astBlockToNotionBlocks(blk)
		out = append(out, nb...)
	}
	return out
}

// astBlockToNotionBlocks converts one [ast.Block] to one or more [NotionBlock]s.
// Tables expand into a parent table block + row child blocks.
func astBlockToNotionBlocks(blk ast.Block) []NotionBlock {
	nb := notionBlockFromAST(blk)
	return nb
}

func notionBlockFromAST(blk ast.Block) []NotionBlock {
	switch blk.Kind {
	case ast.BlockKindHeading:
		return []NotionBlock{notionHeadingBlock(blk)}
	case ast.BlockKindParagraph:
		return []NotionBlock{notionParagraphBlock(blk)}
	case ast.BlockKindCode:
		return []NotionBlock{notionCodeBlock(blk)}
	case ast.BlockKindTable:
		return notionTableBlocks(blk)
	case ast.BlockKindCallout:
		return []NotionBlock{notionCalloutBlock(blk)}
	case ast.BlockKindEmbed:
		return []NotionBlock{notionEmbedBlock(blk)}
	case ast.BlockKindFreeform:
		return []NotionBlock{notionFreeformBlock(blk)}
	case ast.BlockKindStaleBanner:
		return []NotionBlock{notionStaleBannerBlock(blk)}
	default:
		// Unknown kind → paragraph with raw content notation.
		return []NotionBlock{{
			Object:     "block",
			Type:       "paragraph",
			ExternalID: string(blk.ID),
			paragraph: &notionParagraph{
				RichText: notionRichText(fmt.Sprintf("[unknown block kind: %s]", blk.Kind)),
			},
		}}
	}
}

func notionHeadingBlock(blk ast.Block) NotionBlock {
	if blk.Content.Heading == nil {
		return emptyParagraphBlock(blk.ID)
	}
	level := blk.Content.Heading.Level
	if level > 3 {
		level = 3
	}
	if level < 1 {
		level = 1
	}
	notionType := fmt.Sprintf("heading_%d", level)
	h := &notionHeading{RichText: notionRichText(blk.Content.Heading.Text)}
	nb := NotionBlock{
		Object:     "block",
		Type:       notionType,
		ExternalID: string(blk.ID),
	}
	switch notionType {
	case "heading_1":
		nb.heading1 = h
	case "heading_2":
		nb.heading2 = h
	case "heading_3":
		nb.heading3 = h
	}
	return nb
}

func notionParagraphBlock(blk ast.Block) NotionBlock {
	text := ""
	if blk.Content.Paragraph != nil {
		text = blk.Content.Paragraph.Markdown
	}
	return NotionBlock{
		Object:     "block",
		Type:       "paragraph",
		ExternalID: string(blk.ID),
		paragraph:  &notionParagraph{RichText: notionRichText(text)},
	}
}

func notionCodeBlock(blk ast.Block) NotionBlock {
	lang := ""
	body := ""
	if blk.Content.Code != nil {
		lang = notionCodeLanguage(blk.Content.Code.Language)
		body = blk.Content.Code.Body
	}
	return NotionBlock{
		Object:     "block",
		Type:       "code",
		ExternalID: string(blk.ID),
		code:       &notionCode{RichText: notionRichText(body), Language: lang},
	}
}

// notionTableBlocks emits a parent table block and child table_row blocks.
// Notion requires table rows to be nested children of the table block.
// Block ID is applied to the parent table block; row blocks get derived IDs.
func notionTableBlocks(blk ast.Block) []NotionBlock {
	if blk.Content.Table == nil {
		return nil
	}
	t := blk.Content.Table
	colCount := len(t.Headers)

	// Build row blocks.
	var rows []NotionBlock
	// Header row.
	if len(t.Headers) > 0 {
		cells := make([][]notionRichTextItem, len(t.Headers))
		for i, h := range t.Headers {
			cells[i] = notionRichText(h)
		}
		rows = append(rows, NotionBlock{
			Object:   "block",
			Type:     "table_row",
			tableRow: &notionTableRow{Cells: cells},
		})
	}
	// Data rows.
	for _, row := range t.Rows {
		cells := make([][]notionRichTextItem, len(row))
		for i, cell := range row {
			cells[i] = notionRichText(cell)
		}
		rows = append(rows, NotionBlock{
			Object:   "block",
			Type:     "table_row",
			tableRow: &notionTableRow{Cells: cells},
		})
	}

	tableBlock := NotionBlock{
		Object:     "block",
		Type:       "table",
		ExternalID: string(blk.ID),
		table: &notionTable{
			TableWidth:      colCount,
			HasColumnHeader: len(t.Headers) > 0,
			HasRowHeader:    false,
		},
		children: rows,
	}
	return []NotionBlock{tableBlock}
}

func notionCalloutBlock(blk ast.Block) NotionBlock {
	emoji := calloutKindToNotionEmoji(blk.Content.Callout)
	text := ""
	if blk.Content.Callout != nil {
		text = blk.Content.Callout.Body
	}
	return NotionBlock{
		Object:     "block",
		Type:       "callout",
		ExternalID: string(blk.ID),
		callout: &notionCallout{
			Icon:     notionCalloutIcon{Type: "emoji", Emoji: emoji},
			RichText: notionRichText(text),
		},
	}
}

// calloutKindToNotionEmoji maps AST callout kinds to emoji.
func calloutKindToNotionEmoji(c *ast.CalloutContent) string {
	if c == nil {
		return "💡"
	}
	switch strings.ToLower(c.Kind) {
	case "warning", "danger":
		return "⚠️"
	case "tip":
		return "💡"
	case "note":
		return "📝"
	default:
		return "ℹ️"
	}
}

func notionEmbedBlock(blk ast.Block) NotionBlock {
	url := ""
	if blk.Content.Embed != nil {
		// Embed references are page/block IDs, not actual URLs. We encode as
		// a sourcebridge:// scheme URI so round-trip is possible.
		if blk.Content.Embed.TargetBlockID != "" {
			url = fmt.Sprintf("sourcebridge://page/%s/block/%s",
				blk.Content.Embed.TargetPageID,
				blk.Content.Embed.TargetBlockID)
		} else {
			url = fmt.Sprintf("sourcebridge://page/%s", blk.Content.Embed.TargetPageID)
		}
	}
	return NotionBlock{
		Object:     "block",
		Type:       "embed",
		ExternalID: string(blk.ID),
		embed:      &notionEmbed{URL: url},
	}
}

func notionFreeformBlock(blk ast.Block) NotionBlock {
	text := ""
	if blk.Content.Freeform != nil {
		text = blk.Content.Freeform.Raw
	}
	return NotionBlock{
		Object:     "block",
		Type:       "paragraph",
		ExternalID: string(blk.ID),
		paragraph:  &notionParagraph{RichText: notionRichText(text)},
	}
}

func notionStaleBannerBlock(blk ast.Block) NotionBlock {
	text := "This page may be out of date."
	if blk.Content.StaleBanner != nil {
		s := blk.Content.StaleBanner
		syms := strings.Join(s.TriggeringSymbols, ", ")
		text = fmt.Sprintf("This page may be out of date. Recent changes to %s (commit %s) may affect this content.", syms, s.TriggeringCommit)
		if s.RefreshURL != "" {
			text += " Refresh: " + s.RefreshURL
		}
	}
	return NotionBlock{
		Object:     "block",
		Type:       "callout",
		ExternalID: string(blk.ID),
		callout: &notionCallout{
			Icon:     notionCalloutIcon{Type: "emoji", Emoji: "⚠️"},
			RichText: notionRichText(text),
		},
	}
}

func emptyParagraphBlock(id ast.BlockID) NotionBlock {
	return NotionBlock{
		Object:     "block",
		Type:       "paragraph",
		ExternalID: string(id),
		paragraph:  &notionParagraph{RichText: notionRichText("")},
	}
}

