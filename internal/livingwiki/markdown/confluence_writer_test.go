// TestConfluence_SinkOverlay_ComposedBeforeWrite verifies that the caller can
// compose canonical + overlay[confluence] before writing, and that the divergent
// block content appears in the output.
func TestConfluence_SinkOverlay_ComposedBeforeWrite(t *testing.T) {
	client := markdown.NewMemoryConfluenceClient()
	writer := markdown.NewConfluenceWriter(client, markdown.ConfluenceWriterConfig{})

	// Canonical page.
	canonical := ast.Page{
		ID: "arch.auth",
		Manifest: manifest.DependencyManifest{PageID: "arch.auth"},
		Blocks: []ast.Block{
			{
				ID:   "b001",
				Kind: ast.BlockKindParagraph,
				Content: ast.BlockContent{
					Paragraph: &ast.ParagraphContent{Markdown: "Canonical content."},
				},
				Owner: ast.OwnerGenerated,
			},
		},
	}

	// Confluence overlay with divergent content for b001.
	overlay := ast.SinkOverlay{
		SinkName: "confluence-test",
		PageID:   "arch.auth",
		Blocks: map[ast.BlockID]ast.BlockContent{
			"b001": {
				Paragraph: &ast.ParagraphContent{Markdown: "Confluence-specific divergent content."},
			},
		},
		Provenance: map[ast.BlockID]ast.OverlayMeta{
			"b001": {EditedBy: "pm@example.com", EditedAt: time.Now()},
		},
	}

	// Compose: canonical + overlay[confluence].
	composed := ast.ComposeForSink(canonical, overlay)

	// Write the composed page.
	if err := writer.WritePage(confCtx, composed); err != nil {
		t.Fatalf("WritePage with overlay: %v", err)
	}

	xhtml, _, err := client.GetPage(confCtx, "arch.auth")
	if err != nil {
		t.Fatalf("GetPage: %v", err)
	}
	out := string(xhtml)

	// The Confluence-specific content must appear (not canonical).
	if !strings.Contains(out, "Confluence-specific divergent content") {
		t.Error("overlay content missing from Confluence page")
	}
	if strings.Contains(out, "Canonical content") {
		t.Error("canonical content appeared instead of overlay content")
	}
}

// TestConfluence_WriteXHTML_XMLEscaping verifies XML special characters in
// content are properly escaped.
func TestConfluence_WriteXHTML_XMLEscaping(t *testing.T) {
	page := ast.Page{
		ID: "arch.test",
		Manifest: manifest.DependencyManifest{PageID: "arch.test"},
		Blocks: []ast.Block{
			{
				ID:   "b001",
				Kind: ast.BlockKindParagraph,
				Content: ast.BlockContent{
					Paragraph: &ast.ParagraphContent{
						Markdown: `A < B & C > D`,
					},
				},
				Owner: ast.OwnerGenerated,
			},
		},
	}

	var buf bytes.Buffer
	if err := markdown.WriteXHTML(&buf, page); err != nil {
		t.Fatalf("WriteXHTML: %v", err)
	}
	out := buf.String()

	// Unescaped '<' and '>' must not appear in text content context.
	// We look for the pattern with surrounding spaces/content to avoid
	// matching legitimate tag characters.
	if strings.Contains(out, "A < B") {
		t.Error("unescaped < in output paragraph content")
	}
	if strings.Contains(out, "C > D") {
		t.Error("unescaped > in output paragraph content")
	}
	// Escaped forms must be present.
	if !strings.Contains(out, "&lt;") {
		t.Error("< not escaped to &lt;")
	}
	if !strings.Contains(out, "&gt;") {
		t.Error("> not escaped to &gt;")
	}
	if !strings.Contains(out, "&amp;") {
		t.Error("& not escaped to &amp;")
	}
}
