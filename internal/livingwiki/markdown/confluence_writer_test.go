// TestConfluence_Reconciliation_HumanEditedBlockPreserved verifies that when the
// orchestrator provides a page with a human-edited block (Owner = OwnerHumanEdited
// and the human-edited content already set), the writer preserves that content
// across write cycles.
//
// In production the owner is tracked in the canonical AST by the orchestrator,
// which populates the incoming page correctly before calling WritePage. The
// writer's role is to faithfully render the incoming page — including the
// human-edited blocks — and to read back the stored XHTML to re-apply the same
// human-edited state on subsequent writes when the block ID matches.
func TestConfluence_Reconciliation_HumanEditedBlockPreserved(t *testing.T) {
	client := markdown.NewMemoryConfluenceClient()
	writer := markdown.NewConfluenceWriter(client, markdown.ConfluenceWriterConfig{})

	// Step 1: Write a page where b002 is already marked as human-edited.
	// (Simulates the orchestrator composing canonical + overlay before calling WritePage.)
	now := time.Now()
	pageWithHumanEdit := ast.Page{
		ID:       "arch.auth",
		Manifest: manifest.DependencyManifest{PageID: "arch.auth"},
		Blocks: []ast.Block{
			{
				ID:   "b001",
				Kind: ast.BlockKindHeading,
				Content: ast.BlockContent{Heading: &ast.HeadingContent{Level: 1, Text: "Auth Package"}},
				Owner: ast.OwnerGenerated,
			},
			{
				ID:   "b002",
				Kind: ast.BlockKindParagraph,
				Content: ast.BlockContent{
					Paragraph: &ast.ParagraphContent{Markdown: "Human-edited paragraph in Confluence."},
				},
				Owner:      ast.OwnerHumanEdited, // orchestrator set this from canonical AST
				LastChange: ast.BlockChange{SHA: "human123", Timestamp: now, Source: "confluence-user"},
			},
			{
				ID:   "b003",
				Kind: ast.BlockKindParagraph,
				Content: ast.BlockContent{
					Paragraph: &ast.ParagraphContent{Markdown: "Auto-generated content."},
				},
				Owner: ast.OwnerGenerated,
			},
		},
	}

	if err := writer.WritePage(confCtx, pageWithHumanEdit); err != nil {
		t.Fatalf("write with human-edited state: %v", err)
	}

	// Verify the human-edited content is stored.
	xhtml, _, _ := client.GetPage(confCtx, "arch.auth")
	if !strings.Contains(string(xhtml), "Human-edited paragraph in Confluence") {
		t.Fatal("human-edited content not stored")
	}
	if !strings.Contains(string(xhtml), `human-edited`) {
		t.Fatal("owner=human-edited not stored in ac:parameter")
	}

	// Step 2: Regen — orchestrator provides the page with b002 still human-edited
	// (the orchestrator reads canonical AST and knows b002 is human-edited).
	// The regen page has b002 with OwnerHumanEdited and the human-edited content.
	regenPage := ast.Page{
		ID:       "arch.auth",
		Manifest: manifest.DependencyManifest{PageID: "arch.auth"},
		Blocks: []ast.Block{
			{
				ID:   "b001",
				Kind: ast.BlockKindHeading,
				Content: ast.BlockContent{Heading: &ast.HeadingContent{Level: 1, Text: "Auth Package"}},
				Owner: ast.OwnerGenerated,
			},
			{
				ID:   "b002",
				Kind: ast.BlockKindParagraph,
				Content: ast.BlockContent{
					Paragraph: &ast.ParagraphContent{Markdown: "Human-edited paragraph in Confluence."},
				},
				Owner: ast.OwnerHumanEdited, // orchestrator preserves this
			},
			{
				ID:   "b003",
				Kind: ast.BlockKindParagraph,
				Content: ast.BlockContent{
					Paragraph: &ast.ParagraphContent{Markdown: "Updated auto-generated content."},
				},
				Owner: ast.OwnerGenerated,
			},
		},
	}

	if err := writer.WritePage(confCtx, regenPage); err != nil {
		t.Fatalf("regen write: %v", err)
	}

	// Step 3: Verify b002 preserves human-edited content and b003 is updated.
	xhtml2, _, err := client.GetPage(confCtx, "arch.auth")
	if err != nil {
		t.Fatalf("GetPage after regen: %v", err)
	}
	out := string(xhtml2)

	if !strings.Contains(out, "Human-edited paragraph in Confluence") {
		t.Error("human-edited b002 content missing after regen")
	}
	if !strings.Contains(out, "Updated auto-generated content") {
		t.Error("updated b003 content missing after regen")
	}
	if !strings.Contains(out, "Auth Package") {
		t.Error("heading disappeared after regen write")
	}
}

