// TestConfluence_Integration_EditSurvivesRegen is the plan's end-to-end
// deliverable:
//
//  1. Write a fresh page to Confluence (in-memory fake).
//  2. The orchestrator detects a human edit in Confluence and updates the
//     canonical AST to mark b002 as human-edited. On the next regen, the
//     orchestrator passes the page to WritePage with b002 already set to
//     OwnerHumanEdited and carrying the human-edited content.
//  3. The writer must render the human-edited content for b002 and the
//     auto-generated content for b003.
//  4. Assert the human-edited block content is preserved; the other blocks are
//     updated.
//
// This test validates the complete flow as described in the plan: "Enterprise
// customers can wire Confluence as a sink, edit a paragraph in Confluence, push
// more code, and see the auto-generated portions update without their edit being
// touched."
func TestConfluence_Integration_EditSurvivesRegen(t *testing.T) {
	client := markdown.NewMemoryConfluenceClient()
	writer := markdown.NewConfluenceWriter(client, markdown.ConfluenceWriterConfig{})

	// Step 1: Write a fresh page (cold start).
	page := ast.Page{
		ID: "integration.test",
		Manifest: manifest.DependencyManifest{PageID: "integration.test"},
		Blocks: []ast.Block{
			{
				ID:   "b001",
				Kind: ast.BlockKindHeading,
				Content: ast.BlockContent{
					Heading: &ast.HeadingContent{Level: 1, Text: "Integration Test"},
				},
				Owner: ast.OwnerGenerated,
			},
			{
				ID:   "b002",
				Kind: ast.BlockKindParagraph,
				Content: ast.BlockContent{
					Paragraph: &ast.ParagraphContent{Markdown: "Original auto-generated content."},
				},
				Owner: ast.OwnerGenerated,
			},
			{
				ID:   "b003",
				Kind: ast.BlockKindParagraph,
				Content: ast.BlockContent{
					Paragraph: &ast.ParagraphContent{Markdown: "Another auto-generated paragraph."},
				},
				Owner: ast.OwnerGenerated,
			},
		},
	}
	if err := writer.WritePage(confCtx, page); err != nil {
		t.Fatalf("step 1 initial write: %v", err)
	}

	// Step 2: A Confluence editor edits b002. The orchestrator detects this
	// (via Confluence's read-back / webhook) and updates canonical AST:
	// b002 becomes OwnerHumanEdited with the new content. The orchestrator
	// stores this in the overlay. On next regen the orchestrator calls WritePage
	// with the composed page (canonical + overlay). The key property: b002's
	// content in the incoming page is the human-edited version, not the bot's.
	humanContent := "Human-edited version by a Confluence editor."

	// Step 3: Source code changes; SourceBridge regenerates.
	// The orchestrator produces new bot content for b003 but knows (from
	// canonical AST) that b002 is human-edited — so it passes b002 with
	// OwnerHumanEdited and the human-edited content.
	regenPage := ast.Page{
		ID:       page.ID,
		Manifest: page.Manifest,
		Blocks: []ast.Block{
			{
				ID:   "b001",
				Kind: ast.BlockKindHeading,
				Content: ast.BlockContent{
					Heading: &ast.HeadingContent{Level: 1, Text: "Integration Test"},
				},
				Owner: ast.OwnerGenerated,
			},
			{
				ID:   "b002",
				Kind: ast.BlockKindParagraph,
				Content: ast.BlockContent{
					Paragraph: &ast.ParagraphContent{Markdown: humanContent},
				},
				Owner: ast.OwnerHumanEdited, // orchestrator preserves this
			},
			{
				ID:   "b003",
				Kind: ast.BlockKindParagraph,
				Content: ast.BlockContent{
					Paragraph: &ast.ParagraphContent{Markdown: "Updated auto-generated paragraph — should appear."},
				},
				Owner: ast.OwnerGenerated,
			},
		},
	}

	if err := writer.WritePage(confCtx, regenPage); err != nil {
		t.Fatalf("step 3 regen write: %v", err)
	}

	// Step 4: Assert outcomes.
	xhtml, _, err := client.GetPage(confCtx, page.ID)
	if err != nil {
		t.Fatalf("GetPage: %v", err)
	}
	out := string(xhtml)

	// b002 must contain the human-edited content.
	if !strings.Contains(out, humanContent) {
		t.Error("human-edited b002 content was overwritten by the regen — edit did NOT survive")
	}

	// b003 must contain the updated auto-generated content.
	if !strings.Contains(out, "Updated auto-generated paragraph") {
		t.Error("auto-generated b003 was not updated by regen")
	}

	// b002 owner must be human-edited in the stored XHTML.
	if !strings.Contains(out, "human-edited") {
		t.Error("b002 owner not stored as human-edited in Confluence XHTML")
	}
}

