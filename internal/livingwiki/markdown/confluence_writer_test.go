// TestConfluence_RoundTrip_BlockIDsPreserved writes a page to XHTML and parses
// the block IDs back. All IDs must survive the round-trip.
func TestConfluence_RoundTrip_BlockIDsPreserved(t *testing.T) {
	page := confluenceTestPage()

	var buf bytes.Buffer
	if err := markdown.WriteXHTML(&buf, page); err != nil {
		t.Fatalf("WriteXHTML: %v", err)
	}

	// Use the SinkWriter path (same rendering).
	var buf2 bytes.Buffer
	var sw markdown.SinkWriter = markdown.ConfluenceSinkWriter{}
	if err := sw.WritePage(&buf2, page); err != nil {
		t.Fatalf("SinkWriter.WritePage: %v", err)
	}

	// The two outputs must be identical (same render path).
	if buf.String() != buf2.String() {
		t.Error("WriteXHTML and SinkWriter.WritePage produced different output")
	}

	// Verify block IDs appear in the XHTML.
	out := buf.String()
	for _, blk := range page.Blocks {
		if !strings.Contains(out, string(blk.ID)) {
			t.Errorf("block ID %q not found in XHTML output", blk.ID)
		}
	}
}

